// Função de multiplicação
function multiplicacao(a: number, b: number): number {
    return a * b;
}

// Função de saudação
function saudacao(nome: string): string {
    return "Olá " + nome;
}

// Testando as funções
console.log(multiplicacao(5, 3)); // Deve imprimir 15
console.log(saudacao("Douglas"));   // Deve imprimir "Olá Douglas"
